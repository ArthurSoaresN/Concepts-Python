# Gerenciamento de pacotes

# Pacotes são modulos que podem ser instalados e utilizados
# nos programas python
# Eles permitem que você utilize código que foi escrito por outras pessoas
# economizando tempo e esforço

# Pip é o gerenciador de pacotes do python, eles nos permite instalar
# atualizar e remover pacotes
# pypi.org -> baixar pacotes para usar no python

# Ambientes Virtuais
