# Gerenciamento de pacotes

# Pacotes são modulos que podem ser instalados e utilizados
# nos programas python
# Eles permitem que você utilize código que foi escrito por outras pessoas
# economizando tempo e esforço

# Pip é o gerenciador de pacotes do python, eles nos permite instalar
# atualizar e remover pacotes
# pypi.org -> baixar pacotes para usar no python

# Ambientes Virtuais
# python3 -m venv myenv
# source myenv/bin/activate

# Pipenv é uma ferramenta de gerenciamento de pacotes
# que combina a gestão de dependências com a criação 
# de ambiente virtual para seus projetos e adiciona ou remove
# automaticamente do arquivo Pipfile

# pip install pipenv
# pipenv install numpy

# Poetry é outra ferramente da gerenciamento de dependencias para python
# permite declarar as bibliotecas
# pip install poetry
# poetry new myproject

# Boas Práticas para PEP 8
# snake_case para funções, CamelCase para classes, 4 espaços para identação, linhas até 79 caracteres